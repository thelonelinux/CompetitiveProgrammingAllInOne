    static String isBalanced(String s) {

        String ansYes="YES";
        String ansNo="NO";
        String ans;


        Stack<Character> stack=new Stack<>();

        for(int i=0;i<s.length();i++){
            if(s.charAt(i)=='{' || s.charAt(i)=='[' ||  s.charAt(i)=='(' ){
                stack.push(s.charAt(i));
            }
            else {

                char c=stack.peek();
                if(s.charAt(i)==')'  && c=='('){
                    stack.pop();
                }
                else if(s.charAt(i)=='}'  && c=='{'){
                    stack.pop();
                }
                else if(s.charAt(i)==']'  && c=='['){
                    stack.pop();
                }
                else{
                    ans=ansNo;
                    break;
                }
            }
        }



        if(stack.isEmpty()){
            ans= ansYes;
        }
        else{
            ans= ansNo;
        }

        return ans;
    }

